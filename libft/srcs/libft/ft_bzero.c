#include "../../ashes.h"

void	ft_bzero(void *s, size_t n)
{
	char	*copy;

	copy = s;
	if (s != NULL)
	{
		while (n != 0)
		{
			*copy = 0;
			copy++;
			n--;
		}
	}
}
