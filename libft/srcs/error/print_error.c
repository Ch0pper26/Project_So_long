#include "../../ashes.h"

int	ft_print_error(char *str)
{
	ft_putstr_fd("Error\n", 2);
	if (str[0] != '\0')
	{
		ft_putstr_fd(str, 2);
		ft_putstr_fd("\n", 2);
	}
	return (1);
}
